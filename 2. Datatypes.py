#List [] : in python, it is a collection of items that are 
#           ordered and mutable(changeable) , 
#           allows duplicate and 
#           contains various data types.

List =  [1,2,"abc",True,22.23]
print (List)

# Tuples () : ordered, immutable(unchangeable), 
#             allows duplicate elements, contains various data types
Tuple = (3, 1, 4, 6, 3, 'Test', True, 10.1)
print (Tuple)

# Dictionary {} : 'key'- value pair, ordered, mutable, 
#                 does not allow duplicate keys,
#                 and contains various data types.
Dictionary = { 'username': "JohnK",
        'name':"John korah", 
        'age':48, 
        'nationality':False, 
        'blood':'O+',
        'height': 5.11}
print (Dictionary)

# Set {} : unordered, unindexed, mutable, 
#           does not allow duplicate values,
#           and contains various data types.
# Sets {} : unordered, mutable, no duplicate , and contains various data types
mySet = {2, 3, 4, 4, 4, 4, 4, 4, 4,  'True', 1 , 4, 5, 6}
print (mySet)